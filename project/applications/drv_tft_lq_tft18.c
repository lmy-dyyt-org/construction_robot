void TFTSPI_Fill_Area(uint8_t xs,uint8_t ys,uint8_t xe,uint8_t ye,uint16_t color)
{
  unsigned int i,j;
  
  TFTSPI_Set_Pos(xs,ys,xe,ye);
  for(i=0;i<(xe-xs);i++)  
  {
    for(j=0;j<(ye-ys);j++)
    {
      TFTSPI_Write_Word(color);
    }
  }
}

